		<h1>What I collect from you</h1>
		<ul>
			<li>
				<strong>The details you enter upon registration</strong>
				<ul>
					<li>I need these details so that you can use my applications. Without them you would not be able to sign in.</li>
					<li>I may also use these details to get in touch with you. For example, if there is an issue regarding my application, to request feedback on your experience using it, to thank you for using my app, or to say 'hi.' :)</li>
					<li>I may also use your email address with the <a href="https://en.gravatar.com/">Gravatar</a> service. This allows me to use your profile info, for example, to display your profile picture to you and other users of my apps.</li>
				</ul>		
			</li>

			<li>
				<strong>Data that you enter into my applications</strong>
				<ul>
					<li>The data that you enter into my applications is stored in my databases. If it wasn't, you would lose all of your data whenever you refreshed the page in your browser. That wouldn't be very helpful. :)</li>
				</ul>
			</li>
		</ul>

		<h1>Who I share it with</h1>
		<p>I respect that your data may be confidential and will try not to let others see it, unless I receive your permission. Actually, I am uncomfortable with the fact that I have access to your data. But I need to have access to it in order for my applications to work. I may also view it in order to try to improve my applications and become a better programmer, or to see how much my apps are getting used.</p>
		<p>Unless you tell me otherwise, I will assume any communication with me does not contain confidential information, and I may share it. For example, if you send me a feature request, I may share it with friends, family, or other programmers for brainstorming.</p>
		<p>And of course if you choose to share something (I may one day create features for my apps that allow data sharing), it will be shared with whom you choose to share it with.</p>

